import React, { useContext, useEffect } from 'react'
import { AdminContext } from '../../Context/AdminContext'

const DoctorsList = () => {

  const { doctors, adminToken, getAllDoctors, changeAvailability } = useContext(AdminContext);
  
  useEffect(() => {
    if (adminToken) {
      getAllDoctors();
    }
  }, [adminToken, getAllDoctors]);


  return (
    <div className='m-5 max-h-[90vh] overflow-y-scroll'>
      <h1 className='text-lg font-medium'>
        All Doctors
      </h1>

      <div className='w-full flex flex-wrap gap-4 pt-5 gap-y-6'>
        {
          doctors.map( (item, index) => (
            <div key={index} className='border border-gray-500 rounded-xl max-w-56 overflow-hidden cursor-pointer group'>
              <img 
                className='bg-inputBg group-hover:bg-bgAlt transition-all duration-500'
                src={item.image} 
                alt="doctor-image" 
              />
              <div className='p-4'>
                <p className='text-text text-lg font-medium'>
                  {item.name}
                </p>
                <p className='text-textSecondary text-sm'>
                  {item.speciality}
                </p>

                <div className='flex items-center mt-2 gap-1 text-sm'>
                  <input 
                    onChange={() => changeAvailability(item._id)}
                    type="checkbox" 
                    checked={item.available}
                  />
                  <p>Available</p>
                </div>
              </div>
            </div>
          ))
        }
      </div>
    </div>
  )
}

export default DoctorsList
